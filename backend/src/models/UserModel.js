// IMPORT
const mongoose = require("mongoose");
// SCHEMA DB
const User = mongoose.Schema({
  username: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  role: {
    type: String,
    required: true,
  },
});

// EXPORT
module.exports = mongoose.model("Users", User);
